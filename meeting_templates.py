def load_templates_config(config_file: str = "templates_config.json") -> dict:
    """Загружает конфигурацию шаблонов"""
    templates = MeetingTemplates(config_file)
    return templates.config

def main():
    """Функция для тестирования и управления шаблонами"""
    print("📝 УПРАВЛЕНИЕ ШАБЛОНАМИ ПРОТОКОЛОВ ВСТРЕЧ")
    print("=" * 50)
    
    templates = MeetingTemplates()
    
    print("Доступные действия:")
    print("1. Показать доступные шаблоны")
    print("2. Просмотреть шаблон")
    print("3. Создать пользовательский шаблон")
    print("4. Тест автоопределения типа встречи")
    print("5. Выход")
    
    choice = input("\nВыберите действие (1-5): ").strip()
    
    if choice == "1":
        available = templates.get_available_templates()
        print(f"\n📋 Встроенные шаблоны: {', '.join(available['builtin'])}")
        print(f"👤 Пользовательские: {', '.join(available['custom'])}")
        
    elif choice == "2":
        template_name = input("Введите название шаблона: ").strip()
        template = templates.get_template(template_name)
        print(f"\n📝 Шаблон '{template_name}':")
        print("-" * 50)
        print(template[:500] + "..." if len(template) > 500 else template)
        
    elif choice == "3":
        name = input("Название нового шаблона: ").strip()
        print("Введите шаблон (завершите вводом пустой строки):")
        lines = []
        while True:
            line = input()
            if not line:
                break
            lines.append(line)
        template_text = "\n".join(lines)
        templates.add_custom_template(name, template_text)
        
    elif choice == "4":
        test_text = input("Введите тестовый текст для определения типа: ")
        detected_type = templates._detect_meeting_type(test_text)
        print(f"🤖 Определенный тип встречи: {detected_type}")
        
    elif choice == "5":
        print("👋 До свидания!")
    else:
        print("❌ Неверный выбор")

if __name__ == "__main__":
    main()
