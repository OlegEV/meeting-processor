#!/usr/bin/env python3
"""
Шаблоны для генерации протоколов встреч
"""

from typing import Dict, Optional
import json
import os

class MeetingTemplates:
    """Класс для управления шаблонами протоколов встреч"""
    
    def __init__(self, templates_config_file: str = "templates_config.json"):
        self.templates_config_file = templates_config_file
        self.load_templates_config()
        
        # Встроенные шаблоны
        self.builtin_templates = {
            "standard": self._standard_template,
            "business": self._business_template,
            "project": self._project_template,
            "standup": self._standup_template,
            "interview": self._interview_template,
            "brainstorm": self._brainstorm_template,
            "review": self._review_template,
            "planning": self._planning_template,
            "technical": self._technical_template,
            "sales": self._sales_template
        }
    
    def load_templates_config(self):
        """Загружает конфигурацию шаблонов из файла"""
        try:
            if os.path.exists(self.templates_config_file):
                with open(self.templates_config_file, "r", encoding="utf-8") as f:
                    self.config = json.load(f)
            else:
                # Создаем конфигурацию по умолчанию
                self.config = {
                    "default_template": "standard",
                    "custom_templates": {},
                    "template_settings": {
                        "include_technical_info": True,
                        "include_file_datetime": True,
                        "language": "ru",
                        "max_tokens": 2000
                    }
                }
                self.save_templates_config()
        except Exception as e:
            print(f"⚠️ Ошибка загрузки конфигурации шаблонов: {e}")
            self.config = {"default_template": "standard", "custom_templates": {}}
    
    def save_templates_config(self):
        """Сохраняет конфигурацию шаблонов в файл"""
        try:
            with open(self.templates_config_file, "w", encoding="utf-8") as f:
                json.dump(self.config, f, ensure_ascii=False, indent=2)
        except Exception as e:
            print(f"⚠️ Ошибка сохранения конфигурации шаблонов: {e}")
    
    def get_template(self, template_type: str = None, transcript: str = "", file_datetime_info: dict = None) -> str:
        """Возвращает подготовленный шаблон"""
        if template_type is None:
            template_type = self.config.get("default_template", "standard")
        
        # Автоопределение типа встречи по ключевым словам
        if template_type == "auto":
            template_type = self._detect_meeting_type(transcript)
            print(f"🤖 Автоопределение типа встречи: {template_type}")
        
        # Проверяем пользовательские шаблоны
        if template_type in self.config.get("custom_templates", {}):
            template = self.config["custom_templates"][template_type]
            print(f"📝 Используется пользовательский шаблон: {template_type}")
        # Проверяем встроенные шаблоны
        elif template_type in self.builtin_templates:
            template = self.builtin_templates[template_type]()
            print(f"📝 Используется встроенный шаблон: {template_type}")
        else:
            print(f"⚠️ Шаблон '{template_type}' не найден, используется стандартный")
            template = self.builtin_templates["standard"]()
        
        # Подготавливаем информацию о дате и времени
        datetime_info = ""
        datetime_display = " - указать на основе контекста или как 'не указано'"
        
        if file_datetime_info and self.config.get("template_settings", {}).get("include_file_datetime", True):
            datetime_info = f"""
Дата встречи: {file_datetime_info['date']} ({file_datetime_info['weekday_ru']})
"""
            datetime_display = f" - {file_datetime_info['datetime_full']}"
        
        # Форматируем шаблон
        try:
            formatted_template = template.format(
                datetime_info=datetime_info,
                datetime_display=datetime_display,
                transcript="{transcript}"  # Оставляем для последующей подстановки
            )
        except KeyError as e:
            print(f"⚠️ Ошибка форматирования шаблона: {e}")
            # Возвращаем шаблон как есть, если форматирование не удалось
            formatted_template = template
        
        return formatted_template
    
    def _detect_meeting_type(self, transcript: str) -> str:
        """Автоматически определяет тип встречи по содержанию.

        Ключевые слова, минимальное число совпадений и шаблон по умолчанию
        читаются из templates_config.json. Если конфиг отсутствует или пуст,
        используется встроенный набор.
        """
        transcript_lower = transcript.lower()

        # Берём ключевые слова из конфигурации, с фоллбэком на встроенный набор
        keywords = self.config.get("meeting_type_keywords") or {
            "standup": ["стендап", "standup", "daily", "что делал", "что буду делать", "блокеры", "вчера", "сегодня"],
            "project": ["проект", "milestone", "дедлайн", "задача", "спринт", "backlog", "риск"],
            "business": ["бюджет", "roi", "прибыль", "стратегия", "клиент", "продажи", "договор"],
            "interview": ["интервью", "кандидат", "опыт работы", "навыки", "почему выбрали", "зарплата"],
            "brainstorm": ["идея", "мозговой штурм", "креатив", "предложение", "как если", "а что если"],
            "review": ["ретроспектива", "что прошло хорошо", "что улучшить", "проблемы", "feedback"],
            "technical": ["код", "архитектура", "баг", "deploy", "api", "database", "server"],
            "sales": ["продажа", "клиент", "сделка", "кво", "конверсия", "лид", "crm"]
        }

        auto_settings = self.config.get("auto_detection_settings", {})
        min_matches = auto_settings.get("min_keyword_matches", 2)
        fallback = auto_settings.get("fallback_template", "standard")

        scores = {}
        for meeting_type, words in keywords.items():
            score = sum(1 for word in words if word in transcript_lower)
            if score >= min_matches:
                scores[meeting_type] = score

        if scores:
            return max(scores, key=scores.get)

        return fallback
    
    def get_available_templates(self) -> dict:
        """Возвращает список всех доступных шаблонов"""
        builtin = list(self.builtin_templates.keys())
        custom = list(self.config.get("custom_templates", {}).keys())
        
        return {
            "builtin": builtin,
            "custom": custom,
            "all": builtin + custom
        }
    
    def add_custom_template(self, name: str, template: str):
        """Добавляет пользовательский шаблон"""
        if "custom_templates" not in self.config:
            self.config["custom_templates"] = {}
        
        self.config["custom_templates"][name] = template
        self.save_templates_config()
        print(f"✅ Добавлен пользовательский шаблон: {name}")
    
