            cursor = conn.cursor()
            cursor.execute("""
                SELECT COUNT(*) as count, status 
                FROM jobs 
                GROUP BY status
            """)
            job_stats = cursor.fetchall()
            
            if job_stats:
                print(f"\nüìã –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –∑–∞–¥–∞—á:")
